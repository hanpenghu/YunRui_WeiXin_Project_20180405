package com.winwin.picreport.Bcontroller.shouHuoQueRen;

public class Detail {

    private String beforeImgUrl;
    private String  icNo;
    private String cusNo2;
    private String name;
    private String prdName;
    private String qty;


    public String getBeforeImgUrl() {
        return beforeImgUrl;
    }

    public Detail setBeforeImgUrl(String beforeImgUrl) {
        this.beforeImgUrl = beforeImgUrl;
        return this;
    }

    public String getIcNo() {
        return icNo;
    }

    public Detail setIcNo(String icNo) {
        this.icNo = icNo;
        return this;
    }

    public String getCusNo2() {
        return cusNo2;
    }

    public Detail setCusNo2(String cusNo2) {
        this.cusNo2 = cusNo2;
        return this;
    }

    public String getName() {
        return name;
    }

    public Detail setName(String name) {
        this.name = name;
        return this;
    }

    public String getPrdName() {
        return prdName;
    }

    public Detail setPrdName(String prdName) {
        this.prdName = prdName;
        return this;
    }

    public String getQty() {
        return qty;
    }

    public Detail setQty(String qty) {
        this.qty = qty;
        return this;
    }
}
